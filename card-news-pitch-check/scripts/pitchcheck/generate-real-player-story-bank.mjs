function buildTopics() {
  const bySlug = new Map(seeds.map((seed) => [seed.slug, seed]));
  const expanded = [...seeds];

  for (const bonus of bonusAngles) {
    const base = bySlug.get(bonus.baseSlug);
    if (!base) throw new Error(`Missing base seed: ${bonus.baseSlug}`);
    expanded.push({
      ...base,
      ...bonus,
      fact: base.fact,
      whyFun: base.whyFun,
      shareTrigger: base.shareTrigger,
      sourceRefs: base.sourceRefs,
      visualNeed: base.visualNeed,
      assetCue: base.assetCue,
      accent: base.accent,
      category: `${base.category}-alternate-hook`,
    });
  }

  if (expanded.length !== 60) {
    throw new Error(`Expected 60 player story topics, got ${expanded.length}.`);
  }

  return expanded.map(topicFromSeed);
}

